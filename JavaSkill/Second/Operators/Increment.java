
public class Increment {

	public static void main(String[] args) {
		int x = 0;
		// prefix increment 
		System.out.println(--x);
		// postfix increment 
		System.out.println(x--);
		// x is 2 
		System.out.println(x);
        
		// x = x + 2
		x %= 2;
		
		// even elements 
		for (int i = 0; i < 10; i+=2) {
			
		}
	}

}
